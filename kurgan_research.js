const {
  Document, Packer, Paragraph, TextRun, Table, TableRow, TableCell,
  AlignmentType, HeadingLevel, BorderStyle, WidthType, ShadingType,
  LevelFormat, PageNumber, PageBreak, VerticalAlign
} = require('docx');
const fs = require('fs');

const COLORS = {
  darkBlue: "1A3A5C",
  midBlue: "2E6DA4",
  lightBlue: "D0E4F5",
  accent: "E8632A",
  lightGray: "F5F5F5",
  medGray: "DDDDDD",
  white: "FFFFFF",
};

const border = (color = COLORS.medGray) => ({ style: BorderStyle.SINGLE, size: 1, color });
const borders = (color) => ({ top: border(color), bottom: border(color), left: border(color), right: border(color) });

function h1(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_1,
    spacing: { before: 360, after: 120 },
    border: { bottom: { style: BorderStyle.SINGLE, size: 8, color: COLORS.accent, space: 6 } },
    children: [new TextRun({ text, bold: true, size: 34, color: COLORS.darkBlue, font: "Arial" })]
  });
}

function h2(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 280, after: 80 },
    children: [new TextRun({ text, bold: true, size: 26, color: COLORS.midBlue, font: "Arial" })]
  });
}

function h3(text) {
  return new Paragraph({
    heading: HeadingLevel.HEADING_3,
    spacing: { before: 200, after: 60 },
    children: [new TextRun({ text, bold: true, size: 22, color: COLORS.accent, font: "Arial" })]
  });
}

function para(text, opts = {}) {
  return new Paragraph({
    spacing: { before: 60, after: 60 },
    children: [new TextRun({ text, size: 22, font: "Arial", ...opts })]
  });
}

function bullet(text, bold = false) {
  return new Paragraph({
    numbering: { reference: "bullets", level: 0 },
    spacing: { before: 40, after: 40 },
    children: [new TextRun({ text, size: 21, font: "Arial", bold })]
  });
}

function labelRow(label, value) {
  return new TableRow({
    children: [
      new TableCell({
        width: { size: 2500, type: WidthType.DXA },
        shading: { fill: COLORS.lightGray, type: ShadingType.CLEAR },
        borders: borders(COLORS.medGray),
        margins: { top: 80, bottom: 80, left: 120, right: 120 },
        children: [new Paragraph({ children: [new TextRun({ text: label, bold: true, size: 20, font: "Arial", color: COLORS.darkBlue })] })]
      }),
      new TableCell({
        width: { size: 6900, type: WidthType.DXA },
        borders: borders(COLORS.medGray),
        margins: { top: 80, bottom: 80, left: 120, right: 120 },
        children: [new Paragraph({ children: [new TextRun({ text: value, size: 20, font: "Arial" })] })]
      })
    ]
  });
}

function infoTable(rows) {
  return new Table({
    width: { size: 9400, type: WidthType.DXA },
    columnWidths: [2500, 6900],
    rows: rows.map(([l, v]) => labelRow(l, v))
  });
}

function sectionDivider(label) {
  return new Table({
    width: { size: 9400, type: WidthType.DXA },
    columnWidths: [9400],
    rows: [new TableRow({
      children: [new TableCell({
        shading: { fill: COLORS.darkBlue, type: ShadingType.CLEAR },
        borders: borders(COLORS.darkBlue),
        margins: { top: 120, bottom: 120, left: 200, right: 200 },
        children: [new Paragraph({
          children: [new TextRun({ text: label, bold: true, size: 28, font: "Arial", color: COLORS.white })]
        })]
      })]
    })]
  });
}

function subDivider(label) {
  return new Table({
    width: { size: 9400, type: WidthType.DXA },
    columnWidths: [9400],
    rows: [new TableRow({
      children: [new TableCell({
        shading: { fill: COLORS.midBlue, type: ShadingType.CLEAR },
        borders: borders(COLORS.midBlue),
        margins: { top: 80, bottom: 80, left: 160, right: 160 },
        children: [new Paragraph({
          children: [new TextRun({ text: label, bold: true, size: 24, font: "Arial", color: COLORS.white })]
        })]
      })]
    })]
  });
}

function sp(n = 1) {
  return Array.from({ length: n }, () => new Paragraph({ spacing: { before: 60, after: 60 }, children: [new TextRun("")] }));
}

