function jkCard(data) {
  const { name, developer, segment, price, area, deadline, sales, target, reputation, positioning, messages, emotion, tone, contentPatterns, strengths, weaknesses, insight } = data;
  return [
    ...sp(1),
    sectionDivider(`ЖК «${name}»`),
    ...sp(1),
    h3("ПАСПОРТ ПРОЕКТА"),
    infoTable([
      ["Застройщик", developer],
      ["Сегмент", segment],
      ["Цены (от)", price],
      ["Район / адрес", area],
      ["Срок сдачи", deadline],
      ["Темп продаж", sales],
      ["Целевая аудитория", target],
      ["Репутация", reputation],
    ]),
    ...sp(1),
    h3("1. Позиционирование"),
    para(positioning),
    h3("2. Ключевые сообщения"),
    ...messages.map(m => bullet(m)),
    h3("3. Через что продают эмоционально"),
    para(emotion),
    h3("4. Тон коммуникации"),
    para(tone),
    h3("5. Контент-паттерны"),
    ...contentPatterns.map(c => bullet(c)),
    h3("6. Сильные стороны коммуникации"),
    ...strengths.map(s => bullet(s, false)),
    h3("7. Слабые стороны / ограничения"),
    ...weaknesses.map(w => bullet(w)),
    h3("8. Интересное наблюдение"),
    para(insight),
    ...sp(1),
  ];
}

